defmodule PPM do

  # write(name, image) The image is a list of rows, each row a list of
  # tuples {R,G,B}. The RGB values are 0-255.

  def write(name, image) do
    height = length(image)
    width = length(List.first(image))
    {:ok, fd} = File.open(name, [:write])
    IO.puts(fd, "P6")
    IO.puts(fd, "#generated by ppm.ex")
    IO.puts(fd, "#{width} #{height}")
    IO.puts(fd, "255")
    rows(image, fd)
    File.close(fd)
  end

  defp rows(rows, fd) do
    Enum.each(rows, fn(r) ->
      colors = row(r)
      IO.write(fd, colors)
    end)
  end

  defp row(row) do
    List.foldr(row, [], fn({:rgb, r, g, b}, a) ->
      [r, g, b | a]
    end)
  end

end

defmodule Color do

  def convert_blue(depth, max) do
    a = depth/max * 4
    x = trunc(a)
    y = trunc(255*(a-x))

    case trunc(a) do
      0-> {:rgb, 0,0,y}
      1-> {:rgb, 0,y,255}
      2-> {:rgb, 0,255,255-y}
      3-> {:rgb, y, 255, 0}
      4-> {:rgb, 255, 255-y, 0}
    end
  end
  def convert_red(depth, max) do
    a = depth/max * 4
    x = trunc(a)
    y = trunc(255*(a-x))

    case trunc(a) do
      0-> {:rgb, y,0,0}
      1-> {:rgb, 255,y,0}
      2-> {:rgb, 255-y,255,0}
      3-> {:rgb, 0, 255, y}
      4-> {:rgb, 0, 255-y, 255}
    end
  end

end


defmodule Mandel do
  def mandelbrot(width, height, x, y, k, max) do
    trans = fn(w, h) ->
      Cmplx.new(x + k * (w - 1), y - k * (h - 1))
    end
    rows(width, height, trans, max, [])
  end

  def rows(_w, 0, _t, _max, acc) do
    #IO.puts("END ROWS")
    acc
  end
  def rows(w, h, t, max, acc) do
    #IO.puts("rows | w:#{w}|h#{h}");
    rows(w, h-1, t, max, [ row(w, h, t, max, []) | acc])
  end

  def row(0,_,_,_, acc) do
    #IO.puts("END LINE");
    acc
  end
  def row(w, h, t, max, acc) do
    #IO.puts("Line | w:#{w}|h#{h}");
    c = t.(w,h)
    depth = Brot.mandelbrot(c, max);
    rgb = Color.convert_red(depth, max);
    #IO.puts(inspect(depth))
    #IO.puts(inspect(rgb))
    row(w-1, h, t, max, [rgb | acc])
  end
end

