defmodule Cmplx do

  def new(r,i) do
    #return the complex number with the real value r and imaginary i
    {:cmplx, r, i}
  end

  def add(a,b) do
    #adds two complex numbers
    {:cmplx, xa, ya} = a
    {:cmplx, xb, yb} = b
    {:cmplx, xa+xb, ya+yb}
  end

  def sqr(a) do
    #squares the complex number
    {:cmplx, x, y} = a
    {:cmplx, x*x - y*y, 2*x*y}
  end

  def abs(a) do
    #the absolute value of the complex number
    {:cmplx, x, y} = a
    :math.sqrt(x*x + y*y)
  end


end

